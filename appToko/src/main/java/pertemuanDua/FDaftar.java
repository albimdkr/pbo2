    private void kodeUserOtomatis() throws SQLException{
            try {
                    st = conn.createStatement();
                    sql = "SELECT * FROM tb_user order by kode_user DESC";
                    rs = st.executeQuery(sql);
                         if (rs.next()) {
                        kodeUser = rs.getString("kode_user").substring(1);
                        kode = "" + (Integer.parseInt(kodeUser) + 1);
                        kosong = "";
//                        switch (kode.length()) {
//                        case 1 :  kosong = "00";
//                        case 2 :  kosong = "0";
//                        case 3 :  kosong = "";
//                        default : {
//                         }
//                     }
                      if (kode.length() == 1){
                          kosong = "00";    
                      } else if (kode.length() == 2){
                          kosong = "0";
                      } else {
                          kosong = "";
                      }
                      
                      tfKodeUser.setText("U" + kosong + kode);
                      } else {
                   tfKodeUser.setText("U001");
                  }
                  rs.close();
                  st.close();
            } catch (NumberFormatException | SQLException e) {
        }
}

